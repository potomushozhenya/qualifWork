function y = eigenmax1(z)
    y1 = -(6*z^4*sqrt(3) - 11*z^4 + 132*sqrt(3)*z^2 - 204*z^2 + 2*sqrt(57*z^8 - 33*sqrt(3)*z^8 + 2304*z^6 - 1332*sqrt(3)*z^6 + 28512*z^4 - 16416*z^4*sqrt(3) + 108864*z^2 - 62208*sqrt(3)*z^2) + 288*sqrt(3) - 432)/(-z^4 + 12*sqrt(3)*z^2 - 12*z^2 - 288*sqrt(3) + 432);
    y2 = (-6*z^4*sqrt(3) + 11*z^4 - 132*sqrt(3)*z^2 + 204*z^2 + 2*sqrt(57*z^8 - 33*sqrt(3)*z^8 + 2304*z^6 - 1332*sqrt(3)*z^6 + 28512*z^4 - 16416*z^4*sqrt(3) + 108864*z^2 - 62208*sqrt(3)*z^2) - 288*sqrt(3) + 432)/(-z^4 + 12*sqrt(3)*z^2 - 12*z^2 - 288*sqrt(3) + 432);
    y = max(abs(y1),abs(y2));
end
